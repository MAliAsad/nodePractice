const os=require("os");

// platform
console.log(os.platform());

// CPU Architecture
console.log(os.arch());

// CPU core info
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// total memory
console.log(os.totalmem());

// home directory
console.log(os.homedir());

// up time
console.log(os.uptime());